package JavaFuncionesBasicas;

public class Constantes {

	public static void main(String[] args) {
		/* CONCEPTO
		 * Las constantes son valores no modificables
		 * DECLARACION DE CONSTANTE
		 * Anteponer a la variable reservada final
		 * La declaracion de las variables por convencion
		 * deben ser declaradas en mayuscula 
		 * */
				
			final double PI=3.1416;
		 
		 /*El mismo no podemos modificar como se muestra acontinuacion
		   si descomentamos nos muestra error*/
		 
			//PI = 3.456;
 
		 //---------------------------------------------------------
		 /*
		  * En una variable la modificacion es posible como se muestra
		  * 
		  * */
	
		  int edad = 26;
		 //podemos modificar
		  edad= 45;

	}

}
